# 3. 难度分级函数
# ============================================================
def grade_difficulty(type_name, book_level, chapter):
    """根据题型名称、书中标签、章节，返回(lv等级, 理由)"""
    # 先查直接指定
    if type_name in DIRECT_LEVEL:
        lv = DIRECT_LEVEL[type_name]
        return lv, DIFFICULTY_STANDARD[lv]

    # 否则按书中标签推理
    if book_level == "基础":
        # 基础题默认 lv1，部分 lv2
        # 检查是否涉及2个以上知识点
        multi_kp = ["结合", "综合", "混合", "化简求值", "实际应用", "应用题", "应用",
                    "求值", "求参数", "已知.*求", "判断.*图象", "性质.*判断"]
        for kw in multi_kp:
            if kw in type_name:
                return "lv2", "基础标签但涉及多知识点/多步骤，升至lv2"
        return "lv1", "单一概念/定义/直接计算，符合lv1标准"

    elif book_level == "培优":
        # 培优题默认 lv3，简单培优 lv2，高难度培优 lv4
        high_complex = ["辅助线", "模型.*应用", "分类讨论", "动点", "动态",
                       "手拉手", "半角", "截长补短", "倍长", "将军饮马", "胡不归",
                       "阿氏圆", "瓜豆", "费马点", "隐形圆", "奔驰模型",
                       "区间最值.*动", "根的分布", "四边形.*动态",
                       "圆幂定理", "一线三等角", "米勒定理",
                       "婆罗摩笈多", "脚拉脚", "等邻边", "三动点", "逆等线段",
                       "十字架模型", "二倍线段", "探照灯"]
        for kw in high_complex:
            if re.search(kw, type_name):
                return "lv4", "培优标签+几何模型/综合构造/动点/压轴级内容，升至lv4"

        medium_complex = ["含参", "求参", "构造", "换元", "裂项", "错位",
                         "待定系数", "整数解", "同解", "无解", "增根", "恒成立",
                         "面积问题", "翻折", "k的几何意义", "对称问题",
                         "方案.*决策", "分段函数", "多函数结合",
                         "恒过定点", "图象判断", "图象变换",
                         "8.*相似", "A.*相似", "射影定理", "费马",
                         "猪蹄", "铅笔", "内外勾", "等积变换",
                         "飞镖", "角平分线.*模型", "老鹰抓小鸡",
                         "中点四边形", "垂美四边形", "构造.*中位线", "构造.*斜边中线",
                         "不规则图形", "格点问题", "蚂蚁爬行", "翻折问题"]
        for kw in medium_complex:
            if re.search(kw, type_name):
                return "lv3", "培优标签+中档难度技巧/模型，符合lv3标准"

        # 相对简单的培优
        simple_up = ["规律", "新运算", "知二推二", "配方.*最值", "配方.*方程",
                     "大小比较", "几何概型", "频率估计概率",
                     "频数分布表.*直方图"]
        for kw in simple_up:
            if re.search(kw, type_name):
                return "lv2", "培优标签但难度较低/技巧单一，降至lv2"

        return "lv3", "培优标签默认lv3"

    elif book_level == "扩展":
        # 扩展题默认 lv4，部分 lv3，部分 lv5
        extreme = ["托勒密", "西姆松", "阿基米德折弦", "探照灯模型",
                  "米勒定理", "胡不归", "阿氏圆", "瓜豆原理",
                  "费马点"]
        for kw in extreme:
            if re.search(kw, type_name):
                return "lv5", "扩展标签+极高难度定理/模型，升至lv5"

        high_ext = ["高次方程", "根式方程", "拆添项", "试根法", "待定系数法",
                   "12345模型", "角平分线定理", "主元法配方",
                   "双十字相乘法", "1/2公式配方"]
        for kw in high_ext:
            if re.search(kw, type_name):
                return "lv4", "扩展标签+高难度方法，符合lv4标准"

        return "lv3", "扩展标签默认lv3（补充知识，但难度可控）"

    return "lv1", "fallback"


def main():
    types = parse_toc_from_text()

    results = []
    stats = {"lv1": 0, "lv2": 0, "lv3": 0, "lv4": 0, "lv5": 0}
    topic_stats = {"代数": {"lv1": 0, "lv2": 0, "lv3": 0, "lv4": 0, "lv5": 0},
                   "几何": {"lv1": 0, "lv2": 0, "lv3": 0, "lv4": 0, "lv5": 0},
                   "函数": {"lv1": 0, "lv2": 0, "lv3": 0, "lv4": 0, "lv5": 0},
                   "统计概率": {"lv1": 0, "lv2": 0, "lv3": 0, "lv4": 0, "lv5": 0}}

    global_id = 0
    for topic, chapter, name, book_level in types:
        global_id += 1
        lv, reason = grade_difficulty(name, book_level, chapter)
        results.append({
            "id": global_id,
            "topic": topic,
            "chapter": chapter,
            "type_name": name,
            "book_level": book_level,
            "difficulty": lv,
            "reason": reason
        })
        stats[lv] += 1
        topic_stats[topic][lv] += 1

    # 输出JSON
    with open(r"D:\first-cc\_math_397_types.json", "w", encoding="utf-8") as f:
        json.dump(results, f, ensure_ascii=False, indent=2)

    # 输出统计
    print(f"=== 397种题型难度分级完成 ===")
    print(f"总计: {len(results)} 条")
    print(f"\n难度分布:")
    for lv in ["lv1", "lv2", "lv3", "lv4", "lv5"]:
        bar = "█" * (stats[lv] // 5)
        print(f"  {lv}: {stats[lv]:4d} ({stats[lv]/len(results)*100:5.1f}%) {bar}")

    print(f"\n各专题难度分布:")
    for topic in ["代数", "几何", "函数", "统计概率"]:
        ts = topic_stats[topic]
        total = sum(ts.values())
        parts = [f"{lv}:{ts[lv]}" for lv in ["lv1","lv2","lv3","lv4","lv5"] if ts[lv] > 0]
        print(f"  {topic}({total}种): {', '.join(parts)}")

    return results

if __name__ == "__main__":
    main()
