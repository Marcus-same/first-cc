ANSWER_SECTION_KEYWORDS = [
    "参考答案", "答案与解析", "试题解析", "答案及解析",
    "答案页", "答案解析", "中考数学试题解析",
]

# 只含答案的小题（单字母或纯数字/表达式），不视为独立题目
ANSWER_ONLY_PATTERN = re.compile(
    r'^[A-D]$|^[A-D][．.]|^\d+$|^[\-\+]\d+$|^x[≥≤><=]|^\d+\.\d+$'
)

