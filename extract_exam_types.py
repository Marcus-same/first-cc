# 3. 匹配引擎
# ============================================================
def build_kw_index(types):
    """构建关键词匹配索引（含IDF权重）"""
    # 第一遍：收集所有关键词及其文档频率
    all_keywords = []
    for t in types:
        keywords = set()
        name = t["type_name"]
        name_clean = re.sub(r'[^一-鿿\w]', '', name)
        keywords.add(name_clean)

        parts = re.split(r'[—\-—·、，,的与和及之—]', name)
        for p in parts:
            p = p.strip()
            if len(p) >= 2:
                keywords.add(p)

        chapter = t["chapter"]
        ch_match = re.search(r'第[一二三四五六七八九十\d]+章\s*(.+)', chapter)
        if ch_match:
            ch_name = ch_match.group(1)
            keywords.add(ch_name)

        all_keywords.append(keywords)

    # 计算文档频率
    N = len(types)
    kw_df = {}
    for kws in all_keywords:
        for kw in kws:
            if len(kw) >= 2:
                kw_df[kw] = kw_df.get(kw, 0) + 1

    # 构建索引（含IDF权重）
    index = []
    for i, t in enumerate(types):
        kw_weights = {}
        for kw in all_keywords[i]:
            if len(kw) < 2:
                continue
            df = kw_df.get(kw, 1)
            idf = __import__('math').log(N / df)
            kw_weights[kw] = len(kw) * idf

        index.append({
            "id": t["id"],
            "topic": t["topic"],
            "chapter": t["chapter"],
            "type_name": t["type_name"],
            "difficulty": t["difficulty"],
            "keywords": kw_weights,  # {kw: weight}
        })
    return index


def match_question(question_text, kw_index, top_n=5, min_score_ratio=0.25):
    """匹配试题到题型（IDF加权关键词匹配）"""
    import math

    # Step 1: 直接关键词匹配（使用IDF权重）
    scores = {}
    for entry in kw_index:
        score = 0.0
        matched = []
        for kw, weight in entry["keywords"].items():
            if kw in question_text:
                score += weight
                matched.append(kw)
        if score > 0:
            tid = entry["id"]
            if tid not in scores or score > scores[tid]["score"]:
                scores[tid] = {**entry, "score": score, "matched_kw": matched}

    # Step 2: 别名扩展匹配（IDF权重版的bonus）
    # 为别名关键词也计算近似的IDF值
    N = len(kw_index)
    alias_matches = {}
    for alias, type_names in ALIAS_MAP.items():
        if alias in question_text:
            # 估算alias的IDF：稀有词给高分
            alias_df = sum(1 for e in kw_index if any(alias in kw for kw in e["keywords"]))
            alias_idf = math.log(N / max(alias_df, 1))
            alias_weight = len(alias) * alias_idf
            for tname in type_names:
                for entry in kw_index:
                    if entry["type_name"] == tname:
                        tid = entry["id"]
                        alias_matches[tid] = alias_matches.get(tid, 0) + alias_weight

    # 合并别名分数
    for tid, bonus in alias_matches.items():
        if tid in scores:
            scores[tid]["score"] += bonus * 2
            scores[tid]["matched_kw"].append("[别名]")
        else:
            for entry in kw_index:
                if entry["id"] == tid:
                    scores[tid] = {**entry, "score": bonus * 2, "matched_kw": ["[别名]"]}
                    break

    # 排序
    sorted_scores = sorted(scores.values(), key=lambda x: x["score"], reverse=True)

    if not sorted_scores:
        return []

    max_score = sorted_scores[0]["score"]
    threshold = max_score * min_score_ratio
    return [s for s in sorted_scores if s["score"] >= threshold][:top_n]


