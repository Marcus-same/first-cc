# 4. 解析docx
# ============================================================
def parse_docx_questions(filepath):
    """从docx提取试题"""
    doc = Document(filepath)
    paragraphs = [p.text.strip() for p in doc.paragraphs]

    questions = []
    current_section = "未知"
    in_answer_section = False  # 是否进入答案汇总区
    i = 0

    while i < len(paragraphs):
        text = paragraphs[i]

        # 检测答案页特征
        is_answer_header = any(kw in text for kw in ANSWER_SECTION_KEYWORDS)
        if is_answer_header:
            in_answer_section = True
            i += 1
            continue

        # 遇到新的大题型标题，退出答案区
        section_match = re.match(r'^[一二三四五六七八九十]、(.+)$', text)
        if section_match and len(text) <= 25:
            # 如果包含"答案"关键词，确认是答案区
            if any(kw in text for kw in ["答案", "解析", "参考"]):
                in_answer_section = True
            else:
                in_answer_section = False
            current_section = text
            i += 1
            continue

        # 在答案区内跳过所有内容
        if in_answer_section:
            i += 1
            continue

        # 跳过纯答案行（单字母A-D等）
        if ANSWER_ONLY_PATTERN.match(text):
            i += 1
            continue

        # 匹配小题编号
        q_match = re.match(r'^(\d+)[．.、](.+)$', text)
        if q_match:
            q_num = int(q_match.group(1))
            q_text = q_match.group(2).strip()

            # 跳过纯答案文本
            if ANSWER_ONLY_PATTERN.match(q_text) and len(q_text) <= 5:
                i += 1
                continue

            # 收集完整题目（可能跨多段）
            full_parts = [q_text]
            i += 1

            while i < len(paragraphs):
                nt = paragraphs[i]
                if not nt:
                    i += 1
                    continue
                if nt.startswith("【答案】") or nt.startswith("【分析】") or nt.startswith("【详解】") or nt.startswith("【解析】"):
                    break
                if re.match(r'^(\d+)[．.、]', nt):
                    break
                if re.match(r'^[一二三四五六七八九十]、', nt) and len(nt) <= 25:
                    break
                if any(kw in nt for kw in ANSWER_SECTION_KEYWORDS):
                    break
                # 跳过纯选择题选项（A．xxx B．xxx格式）
                if re.match(r'^[A-D][．.]', nt) and len(nt) <= 3:
                    break
                full_parts.append(nt)
                i += 1

            full_text = " ".join(full_parts)
            questions.append({
                "question_num": q_num,
                "section": current_section,
                "text": full_text,
            })
            continue

        i += 1

    return questions


