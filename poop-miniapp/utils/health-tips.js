/* ==================== 生成个性化知识 ==================== */
function generateTip(todaySessions, streak) {
  // 有时间数据 → 个性化生成
  if (todaySessions.length > 0) {
    const mode = Math.random();

    // 1. Bristol 类型解读（权重 25%）
    if (mode < 0.25) {
      const types = [...new Set(todaySessions.filter(s => s.type).map(s => s.type))].sort();
      if (types.length > 0) {
        const t = types[Math.floor(Math.random() * types.length)];
        const pool = TYPE_TEMPLATES[t];
        if (pool) return pool[Math.floor(Math.random() * pool.length)];
      }
    }

    // 2. 时段分析（权重 15%）
    if (mode < 0.40) {
      const hour = new Date(todaySessions[0].start).getHours();
      let periodIdx = 0;
      if (hour < 5) periodIdx = 0;
      else if (hour < 8) periodIdx = 1;
      else if (hour < 12) periodIdx = 2;
      else if (hour < 14) periodIdx = 3;
      else if (hour < 18) periodIdx = 4;
      else if (hour < 22) periodIdx = 5;
      else periodIdx = 6;
      return PERIOD_TIPS[periodIdx];
    }

    // 3. 时长分析（权重 15%）
    if (mode < 0.55) {
      const avgMin = Math.round(todaySessions.reduce((s, x) => s + (x.duration || 0), 0) / todaySessions.length / 60);
      let idx = 0;
      if (avgMin <= 2) idx = 0;
      else if (avgMin <= 5) idx = 1;
      else if (avgMin <= 8) idx = 2;
      else if (avgMin <= 15) idx = 3;
      else idx = 4;
      // Pick the closest matching template
      return DURATION_TIPS[idx](avgMin);
    }

    // 4. 饮食推荐（权重 15%）
    if (mode < 0.70) {
      const food = FOODS[Math.floor(Math.random() * FOODS.length)];
      return `推荐你今天吃 ${food.name}。${food.benefit}。`;
    }
  }

  // 5. 连续打卡鼓励（有 streak 时权重 15%）
  if (streak > 0 && streak <= 150) {
    if (todaySessions.length > 0 || Math.random() < 0.5) {
      const idx = Math.min(streak <= 3 ? 0 : streak <= 7 ? 1 : streak <= 14 ? 2 : streak <= 21 ? 3 : streak <= 30 ? 4 : streak <= 60 ? 5 : streak <= 90 ? 6 : 7, STREAK_TEMPLATES.length - 1);
      return STREAK_TEMPLATES[idx](streak);
    }
  }

  // 6. 健康综合知识（无数据或回退时用）
  const cat = KNOWLEDGE_CATEGORIES[Math.floor(Math.random() * KNOWLEDGE_CATEGORIES.length)];
  return cat[Math.floor(Math.random() * cat.length)];
}

